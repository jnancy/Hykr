package com.example.david.healthyapp;

public class Trail {
    String name;
    String length;
    String difficulty;

    public Trail(String name, String length, String difficulty) {
        this.name=name;
        this.length=length;
        this.difficulty=difficulty;

    }

    public String getName() {
        return name;
    }

    public String getLength() {
        return length;
    }
    public String getDifficulty() {
        return length;
    }
}


